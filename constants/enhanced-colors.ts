// Premium Color Utilities
export const ColorUtils = {
  // Get color with opacity
  withOpacity: (color: string, opacity: number) => {
    if (color.startsWith('#')) {
      const hex = color.slice(1);
      const r = parseInt(hex.substr(0, 2), 16);
      const g = parseInt(hex.substr(2, 2), 16);
      const b = parseInt(hex.substr(4, 2), 16);
      return `rgba(${r}, ${g}, ${b}, ${opacity})`;
    }
    return color;
  },
  
  // Lighten color
  lighten: (color: string, percent: number) => {
    const num = parseInt(color.slice(1), 16);
    const amt = Math.round(2.55 * percent);
    const R = (num >> 16) + amt;
    const G = (num >> 8 & 0x00FF) + amt;
    const B = (num & 0x0000FF) + amt;
    return '#' + (0x1000000 + (R < 255 ? R < 1 ? 0 : R : 255) * 0x10000 +
      (G < 255 ? G < 1 ? 0 : G : 255) * 0x100 +
      (B < 255 ? B < 1 ? 0 : B : 255))
      .toString(16).slice(1);
  },
  
  // Darken color
  darken: (color: string, percent: number) => {
    const num = parseInt(color.slice(1), 16);
    const amt = Math.round(2.55 * percent);
    const R = (num >> 16) - amt;
    const G = (num >> 8 & 0x00FF) - amt;
    const B = (num & 0x0000FF) - amt;
    return '#' + (0x1000000 + (R < 255 ? R < 1 ? 0 : R : 255) * 0x10000 +
      (G < 255 ? G < 1 ? 0 : G : 255) * 0x100 +
      (B < 255 ? B < 1 ? 0 : B : 255))
      .toString(16).slice(1);
  },
  
  // Get contrasting text color
  getContrastColor: (backgroundColor: string) => {
    const color = backgroundColor.slice(1);
    const rgb = parseInt(color, 16);
    const r = (rgb >> 16) & 0xff;
    const g = (rgb >> 8) & 0xff;
    const b = (rgb >> 0) & 0xff;
    const luma = 0.2126 * r + 0.7152 * g + 0.0722 * b;
    return luma < 128 ? '#FFFFFF' : '#000000';
  },
  
  // Generate gradient from color
  generateGradient: (baseColor: string, steps: number = 3) => {
    const colors = [];
    for (let i = 0; i < steps; i++) {
      const factor = i / (steps - 1);
      if (i === 0) {
        colors.push(ColorUtils.lighten(baseColor, 20));
      } else if (i === steps - 1) {
        colors.push(ColorUtils.darken(baseColor, 20));
      } else {
        colors.push(baseColor);
      }
    }
    return colors;
  },
  
  // Create premium shadow
  createShadow: (color: string, intensity: 'small' | 'medium' | 'large' | 'premium' | 'epic' = 'medium') => {
    const baseShadow = EnhancedColors.shadows.light[intensity];
    return {
      ...baseShadow,
      shadowColor: color,
    };
  },
  
  // Blend colors
  blend: (color1: string, color2: string, ratio: number = 0.5) => {
    const hex1 = color1.slice(1);
    const hex2 = color2.slice(1);
    
    const r1 = parseInt(hex1.substr(0, 2), 16);
    const g1 = parseInt(hex1.substr(2, 2), 16);
    const b1 = parseInt(hex1.substr(4, 2), 16);
    
    const r2 = parseInt(hex2.substr(0, 2), 16);
    const g2 = parseInt(hex2.substr(2, 2), 16);
    const b2 = parseInt(hex2.substr(4, 2), 16);
    
    const r = Math.round(r1 * (1 - ratio) + r2 * ratio);
    const g = Math.round(g1 * (1 - ratio) + g2 * ratio);
    const b = Math.round(b1 * (1 - ratio) + b2 * ratio);
    
    return '#' + ((1 << 24) + (r << 16) + (g << 8) + b).toString(16).slice(1);
  },
};

